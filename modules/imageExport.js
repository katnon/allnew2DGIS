/**
 * 📷 이미지 내보내기 모듈 (Image Export Module)
 * 
 * 【모듈 역할】
 * 현재 지도 화면을 이미지 파일로 캡처하여 다운로드할 수 있는 기능을 제공하는 모듈입니다.
 * 
 * 🔄 【정보 흐름 (Data Flow)】
 * 1. UI 버튼 클릭 → exportMapAsImage() 호출
 * 2. 파일명 입력 다이얼로그 표시 → 사용자 입력 대기
 * 3. 지도 렌더링 완료 대기 → 'rendercomplete' 이벤트 수신
 * 4. 지도 컨테이너의 모든 캔버스 레이어 수집
 * 5. 새로운 캔버스에 모든 레이어 순차적 합성
 * 6. PNG 데이터 URL 생성 → 다운로드 링크 생성 → 자동 다운로드 실행
 * 
 * 🎯 【제공 기능】
 * - 📸 지도 스크린샷: 현재 화면의 모든 레이어를 포함한 이미지 생성
 * - 🎨 레이어 합성: 베이스맵, 오버레이, 그리기, 텍스트 등 모든 요소 포함
 * - 💾 자동 다운로드: PNG 형식으로 브라우저 다운로드 폴더에 저장
 * - 🏷️ 파일명 설정: 사용자 지정 파일명 또는 자동 생성 파일명
 * - ⚙️ 고급 옵션: 이미지 품질, 형식, 스케일 조정 (확장 가능)
 * 
 * 📤 【데이터 연동 관계】
 * - 지도 객체 ← main.js 에서 전달
 * - 캔버스 요소들 ← OpenLayers 렌더링 엔진에서 생성
 * - DOM 다운로드 ← 브라우저 다운로드 API
 * 
 * 🔧 【상태 관리】
 * - 내보내기 진행 상태 추적 (중복 실행 방지)
 * - 사용자 설정 저장 경로 관리
 * - 렌더링 완료 대기 및 오류 처리
 */
