/**
 * 📐 측정 도구 관리 모듈 (Measurement Tools Management Module)
 * 
 * 【모듈 역할】
 * 지도 상의 도형에 대한 거리 및 면적 측정 기능을 제공하며, 측정 결과를 오버레이로 표시합니다.
 * 
 * 🔄 【정보 흐름 (Data Flow)】
 * 1. UI 버튼 클릭 → enable*Measure() 함수 호출
 * 2. 지도 클릭 이벤트 등록 → 사용자 피처 클릭 대기
 * 3. forEachFeatureAtPixel() → 클릭된 위치의 피처 검색
 * 4. 피처 도형 타입 분석 → 적절한 계산 함수 호출
 * 5. 측정 결과 계산 → Overlay 생성 → 지도에 표시
 * 6. 토글 기능으로 표시/숨김 제어
 * 
 * 🎯 【제공 기능】
 * - 📏 거리 측정: 선분 길이, 다각형 둘레, 원 둘레
 * - 📊 면적 측정: 다각형 내부 면적, 원 면적
 * - 🏷️ 측정 결과 오버레이: 도형 위에 결과 표시
 * - 🔄 토글 기능: 같은 피처 클릭 시 결과 표시/숨김
 * 
 * 📤 【데이터 흐름 관계】
 * - drawLayer ← drawTools.js 에서 생성된 그리기 레이어
 * - Feature 검색 → OpenLayers forEachFeatureAtPixel()
 * - 측정 결과 → Overlay → 지도 표시
 * - window.gisMap ← main.js 에서 설정
 * 
 * 🔧 【상태 관리】
 * - 측정 모드별 상태 추적 (거리/면적)
 * - 오버레이 목록 관리 (생성/삭제)
 * - 피처별 측정 결과 캐시
 * - 토글 기능용 중복 검사
 */
